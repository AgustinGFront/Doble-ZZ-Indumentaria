function registroYVerificacion() {
    // Bucle
    for(let i = 0; i < 20; i++) {
        console.log(i);
    }
    
    // Pedimos los datos
    let usuario = prompt("Ingrese su usuario");
    let contra = prompt("Ingrese su contraseña");
    
    // Arrays
    const usuarios = [usuario];
    const contrasenas = [contra];
    
    let intentos = 0;
    let verificacionExitosa = false;
    
    // Bucle de verificación con reintentos
    while (!verificacionExitosa && intentos < 3) {
        // Vuelva a ingresar
        let usuarioIngresado = prompt("Ingrese nuevamente su usuario");
        let contraIngresada = prompt("Ingrese nuevamente su contraseña");
        
        // Verificamos
        if (usuarioIngresado === usuarios[0] && contraIngresada === contrasenas[0]) {
            alert("Tu contraseña es correcta ✅");
            verificacionExitosa = true;
            return true;
        } else if (usuarioIngresado !== usuarios[0]) {
            intentos++;
            if (intentos < 3) {
                alert("El usuario es incorrecto ❌. Intenta nuevamente.");
            }
        } else if (contraIngresada !== contrasenas[0]) {
            intentos++;
            if (intentos < 3) {
                alert("La contraseña es incorrecta ❌. Intenta nuevamente.");
            }
        }
    }
    
    // Si agotó los 3 intentos
    if (!verificacionExitosa) {
        alert("Has agotado tus intentos. Inténtalo más tarde.");
        return false;
    }
}

// Ejecutar la función
